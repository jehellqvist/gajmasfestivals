Hamnfestivalen bjuder på fyra festfyllda dagar med upplevelser, musik, mat &amp; kultur. I juli 2014 slog portarna upp till en fantastisk Hamnfestival som arrangerades längs havet i Limhamn. Hamnfestivalen 2014 drog över 15000 besökare och succén var omedelbar. Årets Hamnfestival går av stapeln den 30 juli och pågår t.om. den 2 augusti.

Är du intresserad av att bli en medverkande aktör på Hamnfestivalen?
<h3>Anmälan</h3>
Blankett för anmälan till att medverka på Hamnfestivalen fylls i och skickas via e-post till:

<i class="fa fa-envelope"></i><a href="mailto:info@polimhamn.se?Subject=Anmälan%20medverkande%20aktör">info@polimhamn.se </a>

Tänk på att anmälan sker omgående och är bindande.

Krav på medverkan samtliga dagar. Avtal kommer skrivas med samtliga aktörer. Mer praktisk information delges vid avtalsskrivningen. Bodar och marknadsstånd finns på plats till ert förfogande. Bodarna är till för våra mataktörer och finns i begränsat antal. Övriga aktörer har tillgång till moderna marknadsstånd som har en exponeringsyta på 220x80cm.
<h3>Plats, Öppetider &amp; Pris</h3>
Årets Hamnfestival är primärt förlagd i och kring Småbåtshamnen och Fiskehamnen. Fiskehamnen håller öppet för besökare mellan kl 11.00 - 20.00 alla dagar och Småbåtshamnen mellan kl. 11.00 - 22.00 alla dagar.
